function somar(n, m) {
    console.log(n + m);
}
function subtrair(n, m) {
    console.log(n - m);
}

somar(3, 4);
somar(31, 98);
somar(3, 98);
somar(31, 4);

subtrair(11, 7)
subtrair(19, 52)
